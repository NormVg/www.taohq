async function listFiles(dir, extension) {
  const entries = await readdir(dir)
  return entries.filter((file) => file.endsWith(extension)).sort((a, b) => a.localeCompare(b))
}

async function readText(filePath) {
  const { readFile } = await import('node:fs/promises')
  return readFile(filePath, 'utf8')
}

function findRuntimeImports(source) {
  const matches = [...source.matchAll(/from ['"]([^'"]+)['"]/g)].map((match) => match[1])
  return matches.filter((value) => value.startsWith('../') || value.startsWith('./')).sort()
}

function componentGroup(name) {
  for (const [group, names] of Object.entries(groups)) {
    if (names.includes(name)) return group
  }
  return 'Other'
}

async function sync() {
  const runtimeStat = await stat(runtimeRoot).catch(() => null)
  if (!runtimeStat?.isDirectory()) {
    throw new Error(`Missing runtime source directory: ${runtimeRoot}`)
  }

  await rm(sourceRoot, { recursive: true, force: true })
  await mkdir(path.join(sourceRoot, 'components'), { recursive: true })
  await mkdir(path.join(sourceRoot, 'composables'), { recursive: true })
  await mkdir(path.join(sourceRoot, 'server'), { recursive: true })

  const componentFiles = await listFiles(path.join(runtimeRoot, 'components'), '.vue')
  const composableFiles = (await readdir(path.join(runtimeRoot, 'composables')))
    .filter((file) => file.endsWith('.ts') || file.endsWith('.js'))
    .sort((a, b) => a.localeCompare(b))

  for (const file of componentFiles) {
    await copyFile(path.join(runtimeRoot, 'components', file), path.join(sourceRoot, 'components', file))
  }
  for (const file of composableFiles) {
    await copyFile(path.join(runtimeRoot, 'composables', file), path.join(sourceRoot, 'composables', file))
  }
  await copyFile(path.join(runtimeRoot, 'maya.css'), path.join(sourceRoot, 'maya.css'))
  await copyFile(path.join(runtimeRoot, 'plugin.ts'), path.join(sourceRoot, 'plugin.ts'))

  const componentRows = []
  for (const file of componentFiles) {
    const name = file.replace(/\.vue$/, '')
    const source = await readText(path.join(runtimeRoot, 'components', file))
    const imports = findRuntimeImports(source)
    componentRows.push({
      name,
      file,
      group: componentGroup(name),
      imports: imports.length ? imports.join(', ') : '-',
    })
  }

  const lines = [
    '# Maya UI Source Map',
    '',
    'This file is generated by `skills/maya-design/scripts/sync-source-references.mjs` from `src/runtime`.',
    'Use it as the routing layer before opening exact component source.',
    '',
    '## Source-First Workflow',
    '',
    '1. Read `references/architecture.md` to choose where copied files should live in the target project.',
    '2. Read `references/source/maya.css` when theme tokens, shadows, radii, or animations are needed.',
    '3. Read only the exact component files needed from `references/source/components/`.',
    '4. If a component imports a local composable or child component, copy that dependency too.',
    '5. Keep the copied component source close to the original first, then adapt names, props, styling, or dependencies deliberately.',
    '',
    '## Theme and Runtime Files',
    '',
    '| File | Use when |',
    '|---|---|',
    '| `references/source/maya.css` | Project needs Maya tokens, theme variables, button/input/table surfaces, shadows, or motion variables. |',
    '| `references/source/plugin.ts` | Project is a Nuxt module integration or needs to understand how the package registers components/styles. |',
    '',
    '## Composables',
    '',
    '| Composable | Use when |',
    '|---|---|',
    ...composableFiles.map((file) => {
      const use = file === 'useToast.ts'
        ? 'Toast queue, global notification state, dismiss/update helpers.'
        : file === 'useMayaTheme.ts'
          ? 'Dark/light theme state, persistent theme selection, document theme attributes.'
          : file === 'useMayaSound.ts'
            ? 'Tactile UI sounds for buttons, toggles, dialogs, and alerts.'
            : file === 'useShiki.js'
              ? 'Syntax highlighting for code blocks and docs examples.'
              : 'Component support logic.'
      return `| \`references/source/composables/${file}\` | ${use} |`
    }),
    '',
    '## Components',
    '',
  ]

  for (const group of [...Object.keys(groups), 'Other']) {
    const rows = componentRows.filter((row) => row.group === group)
    if (!rows.length) continue
    lines.push(`### ${group}`, '')
    lines.push('| Component | Source file | Local runtime imports |')
    lines.push('|---|---|---|')
    for (const row of rows) {
      lines.push(`| ${row.name} | \`references/source/components/${row.file}\` | ${row.imports} |`)
    }
    lines.push('')
  }

  await writeFile(path.join(skillRoot, 'references', 'source-map.md'), `${lines.join('\n')}\n`)
}

sync().catch((error) => {
  console.error(error)
  process.exitCode = 1
})
